package psr;

public class Tool {
	private int strength;
	private char type;
	
	public void setStrength(int strength) {
		this.strength = strength;
	}

	public char getType() {
		return type;
	}

	public void setType(char type) {
		this.type = type;
	}
	
	int getStrength() {
		return this.strength;
	}
	
}
